// se utiliza "//" para poner un comentario en la linea desde nonde inicie el misimo

// #include como ya lo dice hace la función de añadir lirerias 
// y en este caso la libreria seria stdio.h que significa entrada y salida estaandar

#include <stdio.h>

// omitiré el int y el void por ahora pero hay que saber que la funcion main() hace que el programa inicie
// por lo que tine que estar en alguna parte del código

int main(void)
{
 // aqui vemos la funcion printf que hace que en la pantalla se escriba la que se encuentra dentro de ella
	
	printf("Hola Mundo - Adios Mundo.\n");// el ";" se utiliza para finalizar cada funcion es por asi decir
	// \n indica que se mostrará esto en una nueva línea   //el punto final de cada oración
	
	return 0; /*acá tenemos return que al finalizar le dice al programa que devuelva algun tipo de
	información en esto caso el 0 que significa que no devuelva nada*/ 

} /* y estos signos que veriamos luego del main y al final son los que encierran las funciones que se 
inician dentro del main (se puede encontrar en muchas otras funciones) */ 

